func Index(w http.ResponseWriter, r *http.Request) {
	const tpl = `
	<!DOCTYPE html>
	<html>
	<head>
		<meta charset="UTF-8">
		<title>GoWeb</title>
	</head>
	<body>
		<h1>Welcome to GoWeb!</h1>
		<p>Please Note the Following Pages to Access</p>
		<ul>
			<li>GET --> /Example 1</li>
			<li>POST --> /Example 2</li>
		</ul>
	</body>
	</html>`
	w.Header().Set("Content-Type", "text/html; charset=utf-8")
	fmt.Fprint(w, tpl)
	return
}

